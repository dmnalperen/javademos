public class DortIslem {
    public int Topla(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int Çıkarma(int sayi1, int sayi2) {
        return sayi1 - sayi2;

    }

    public int Çarpma(int sayi1, int sayi2) {
        return sayi1 * sayi2;
    }

    public int Bölme(int sayi1, int sayi2) {
        return sayi1 / sayi2;
    }
}


